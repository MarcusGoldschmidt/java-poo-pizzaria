    private void jbConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbConsultarActionPerformed
        // TODO add your handling code here:
        
        Produto produto = new Produto();
        ProdutoDao produtoDao = new ProdutoDao();
        
        produto.setIdProduto(Integer.parseInt(JT_ID_PRODUTO.getText()));
        
        this.JT_Nome.setText(produto.getNomeProduto());
        this.JT_Quantidade.setText(String.valueOf(produto.getQuantidade()));
        this.JT_Valor.setText(String.valueOf(produto.getValor()));
        this.JT_Fornecedor.setText(String.valueOf(produto.getForn().getIdFornecedor()));
        this.JT_Funcionario.setText(String.valueOf(produto.getFun().getIdFuncionario()));
        this.JT_DataDeEntrada.setText(String.valueOf(produto.getDataEntrada()));
        this.JT_DataVencimento.setText(String.valueOf(produto.getDataVencimento()));
        
        try {
            produtoDao.ConsultarProduto(produto);
        } catch (SQLException ex) {
            Logger.getLogger(jfCadastroProduto.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_jbConsultarActionPerformed


