    private void jbExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbExcluirActionPerformed
        // TODO add your handling code here:
        Cliente cli = new Cliente();
        ClienteDao clidao = new ClienteDao();
        cli.setNome(JT_Nome.getText());
        cli.setCpf(JT_Cpf.getText());
        cli.setTelefone(JT_Telefone.getText());
        cli.setBairro(JT_Bairro.getText());
        cli.setNumero(JT_Numero.getText());
        cli.setLogradouro(JT_Logradouro.getText());
        cli.setComplemento(JT_Complemento.getText());

        try {
            clidao.remove(cli);
        } catch (SQLException ex) {
            Logger.getLogger(jfCadastroCliente.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_jbExcluirActionPerformed

