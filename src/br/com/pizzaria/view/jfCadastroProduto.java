    private void jbInserirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbInserirActionPerformed
        // TODO add your handling code here:
        Produto produto = new Produto();
        ProdutoDao produtoDao = new ProdutoDao();

        produto.setQuantidade(Integer.parseInt(JT_Quantidade.getText()));
        produto.setValor(Float.parseFloat(JT_Valor.getText()));
        produto.getFun().setIdFuncionario(Integer.parseInt(JT_Funcionario.getText()));
        produto.getForn().setIdFornecedor(Integer.parseInt(JT_Fornecedor.getText()));
        produto.setNomeProduto(JT_Fornecedor.getText());
        produto.setDataEntrada(Date.valueOf(JT_DataDeEntrada.getText()));
        produto.setDataVencimento(Date.valueOf(JT_DataVencimento.getText()));

        try {
            produtoDao.Inserir(produto);
        } catch (SQLException ex) {
            Logger.getLogger(jfCadastroProduto.class.getName()).log(Level.SEVERE, null, ex);
        }


    }//GEN-LAST:event_jbInserirActionPerformed

